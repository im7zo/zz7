# روابط الصور من قناة تليجرام (تغيير الرابط حسب قناتك)
image_links =[f"https://t.me/CNN9N/{i}"for i in range (10 ,143 )]

# أمر .كت - يرسل سؤال عشوائي
@client .on (events .NewMessage (outgoing =True ,pattern =r"^\.كت$"))
async def send_random_question (event ):
    question =random .choice (questions_list )
    await event .edit (question )# يعدل الرسالة بدل حذفها وإرسال رسالة جديدة

# أمر .انمي - يرسل صورة عشوائية من قناة تليجرام
@client.on(events.NewMessage(outgoing=True, pattern=r"^\.انمي$"))
async def send_random_anime(event):
    await event.delete()
    for _ in range(5):
        try:
            link = random.choice(image_links)
            channel, msg_id = link.split("/")[-2:]
            message = await client.get_messages(channel, ids=int(msg_id))

            if message and message.media:  # إذا بي ميديا
                await event.respond(file=message.media)
                break
            else:
                continue
        except Exception as e:
            print(f"خطأ أثناء جلب الصورة: {e}")
            continue